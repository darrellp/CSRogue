using System.Collections.Generic;
using CSRogue.Items;
using CSRogue.Item_Handling;

namespace CSRogue.Interfaces
{
    public interface ILevel
    {
        ////////////////////////////////////////////////////////////////////////////////////////////////////
        /// <summary>   Gets the depth of the level. </summary>
        ///
        /// <value> The depth. </value>
        ////////////////////////////////////////////////////////////////////////////////////////////////////
        int Depth { get; }

        ////////////////////////////////////////////////////////////////////////////////////////////////////
        /// <summary>   Gets the map for the level. </summary>
        ///
        /// <value> The map. </value>
        ////////////////////////////////////////////////////////////////////////////////////////////////////
        IGameMap Map { get; }

        ////////////////////////////////////////////////////////////////////////////////////////////////////
        /// <summary>   Gets the item information. </summary>
        ///
        /// <value> The factory. </value>
        ////////////////////////////////////////////////////////////////////////////////////////////////////

        IItemFactory Factory { get; }

        ////////////////////////////////////////////////////////////////////////////////////////////////////
        /// <summary>   Gets the creatures on this level. </summary>
        ///
        /// <value> The creatures on this level. </value>
        ////////////////////////////////////////////////////////////////////////////////////////////////////

        List<ICreature> Creatures { get; }

        int ItemCount(bool areCreatures);
    }
}